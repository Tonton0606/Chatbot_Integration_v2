/* ─── Helpers ────────────────────────────────────────────────── */
const fmt = (n) =>
  n >= 1_000_000 ? `₱${(n / 1_000_000).toFixed(1)}M`
    : n >= 1_000 ? `₱${(n / 1_000).toFixed(0)}k`
      : `₱${n}`;
const pctFmt = (n) => `${Number(n).toFixed(1)}%`;
const numFmt = (n) => Number(n).toLocaleString();
const calcChange = (arr) => {
  if (!arr || arr.length < 2) return 0;
  const latest = arr.at(-1)?.value ?? 0;
  const previous = arr.at(-2)?.value ?? 0;
  if (!previous) return 0;
  return ((latest - previous) / previous) * 100;
};
const buildFallbackTrend = (days, currentValue, baseline = 0) => {
  const safeCurrent = Number(currentValue) || baseline;
  return Array.from({ length: days }, (_, index) => {
    const d = new Date();
    d.setDate(d.getDate() - (days - index - 1));
    const progress = days <= 1 ? 1 : index / (days - 1);
    const value = safeCurrent > 0 ? Math.max(0, safeCurrent * (0.75 + progress * 0.25)) : 0;
    return { date: d.toLocaleDateString("en-CA"), value };
  });
};

