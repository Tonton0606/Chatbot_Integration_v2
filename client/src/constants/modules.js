export const ALL_CLIENT_MODULES = [
  ...CORE_CLIENT_MODULES,
  ...WORKSPACE_CLIENT_MODULES,
];

export function getModuleByKey(moduleKey) {
  return ALL_CLIENT_MODULES.find((module) => module.key === moduleKey);
}

export function groupModulesBySection(modules) {
  return modules.reduce((sections, module) => {
    const section = module.section || "Other";

    if (!sections[section]) {
      sections[section] = [];
    }

    sections[section].push(module);
    return sections;
  }, {});
}
